package javadecisionmaking;

public class Elseifsample 
{

	public static void main(String[] args) 
	{
		int a=30,b=30;
		
		if(a>b) 
		{
			System.out.println("a is great");
		}
		
		else if(b>a)
		{
			System.out.println("b is great");
		}
		
		else {
			System.out.println("both are equal");
		}

	}

}
